def validate_python_code_blocks(text: str) -> List[str]:
    """
    Extracts triple-backtick python code blocks from text and attempts to compile them.
    Returns a list of syntax errors found.
    """
    errors = []
    pattern = re.compile(r"```python\s*(.*?)\s*```", re.DOTALL)
    blocks = pattern.findall(text)
    for idx, block in enumerate(blocks):
        try:
            ast.parse(block)
        except SyntaxError as e:
            text_snip = e.text or ""
            errors.append(
                f"Syntax error in code block #{idx + 1}:\n"
                f"Line {e.lineno}: {e.msg}\n"
                f"Code snippet with issue:\n"
                f"{text_snip.strip()}"
            )
    return errors


